course = "   Python is the best"
print(course.upper())  # all uppercase letters
print(course.lower())  # all lowercase letters
print(course.title())  # put all capitals on each word
print(course.strip())  # take away the space from begging of the string
print(course.find("is"))  # finding an index(position) of the exact search
print(course.replace("P", "-"))  # replacing all "P" with "-"
print("Python" in course)  # boolean finding word in sentence
print("Python" not in course)  # boolean finding word in sentence
